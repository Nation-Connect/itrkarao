<p><strong>ITR-1:-</strong>&nbsp;Income tax ITR-1 form must be filed by taxpayers who receive salary and have income from a house property.</p>

<p><strong>ITR-2:-</strong>&nbsp;Income tax ITR-2 form can be used for filing income tax return of individuals and HUFs who have DIN or income earned from a business entity.</p>

<p><strong>ITR-3:-</strong>&nbsp;Income tax ITR-3 form can be used for filing income tax return of individuals and HUFs who have income from proprietorship business or profession.</p>

<p><strong>ITR-4:-</strong>&nbsp;Income tax ITR-4 form can be used for filing IT return under the Presumptive Taxation Scheme if business turnover is less than Rs.2 crores if professional income is less than Rs.50 lakhs.</p>

<p><strong>ITR-5:-</strong>&nbsp;Income tax ITR-5 form can be used for filing income tax return of partnership firms, LLPs and AOPs. ITR-5 return may require tax audit based on turnover of the business.</p>

<p><strong>ITR-6:-</strong>&nbsp;Income tax ITR-6 form is used for filing income tax return of private limited company, limited company and one person company registered in India.</p>

<p><strong>ITR-7:-</strong>&nbsp;Income tax ITR-7 form is used for filing income tax return of charitable trusts, political parties, colleges, universities and scientific research institutions registered in India.</p>
